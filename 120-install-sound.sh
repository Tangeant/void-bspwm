func_install() {
	if xbps-query $1 &> /dev/null; then
		tput setaf 2
  		echo "###############################################################################"
  		echo "################## The package "$1" is already installed"
      	echo "###############################################################################"
      	echo
		tput sgr0
	else
    	tput setaf 3
    	echo "###############################################################################"
    	echo "##################  Installing package "  $1
    	echo "###############################################################################"
    	echo
    	tput sgr0
    	sudo xbps-install -vy $1
    fi
}

###############################################################################
echo "Installation of sound software"
###############################################################################

list=(
pulseaudio
alsa-plugins-pulseaudio
pavucontrol
alsa-firmware
alsa-lib
alsa-plugins
alsa-utils
alsa-plugins-ffmpeg
gstreamer1
gst-plugins-good1
gst-plugins-bad1
gst-plugins-base1
gst-plugins-ugly1
playerctl
volumeicon
)

count=0

for name in "${list[@]}" ; do
	count=$[count+1]
	tput setaf 3;echo "Installing package nr.  "$count " " $name;tput sgr0;
	func_install $name
done

###############################################################################

tput setaf 11;
echo "################################################################"
echo "Software has been installed"
echo "################################################################"
echo;tput sgr0
